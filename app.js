"use strict"

/* Operadores Racionales */

var a = 10;
var b = 20;

/*MAYOR QUE: > */
var mayorq = (a > b);
console.log(a + " > " + b + ": " + mayorq);

/*MENOR QUE: < */
var menorq = (a < b);
console.log(a + " < " + b + ": " + menorq);

/*MAYOR O IGUAL QUE: >= */
var migualq = (a >= b);
console.log(a + " >= " + b + ": " + migualq);

/*MENOR O IGUAL QUE: <= */
var menigualq = (a <= b);
console.log(a + " <= " + b + ": " + menigualq);

/*IGUAL QUE: == */
var igualq = (a == b);
console.log(a + " == " + b + ": " + igualq);

/*NO ES IGUAL QUE O ES DIFERENTE QUE: ! */
var nonigualq = (a != b);
console.log(a + " != " + b + ": " + nonigualq);